import numpy as np

def 透视(物体顶点, 视角=108, 宽高比=1, 近平面=2, 远平面=10):
    f = 1 / np.tan(np.radians(视角) / 2)
    透视投影矩阵 = np.array([
        [f / 宽高比, 0, 0, 0],
        [0, f, 0, 0],
        [0, 0, (远平面 + 近平面) / (近平面 - 远平面), (2 * 远平面 * 近平面) / (近平面 - 远平面)],
        [0, 0, -1, 0]
    ], dtype=np.float64)  
    齐次顶点 = np.hstack((物体顶点, np.ones((物体顶点.shape[0], 1), dtype=np.float64)))
    投影结果 = 齐次顶点 @ 透视投影矩阵.T


    投影结果[:, :3] /= 投影结果[:, 3:4]
   
    return 投影结果[:, :3]
